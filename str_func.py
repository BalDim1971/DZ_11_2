# Имитируем исправление ошибки

def str_upper(my_str):
	'''
	Функция преобразует переданную строку в верхний регистр
	:param my_str: базовая строка
	:return: преобразованная строка
	'''
	return my_str.upper()


def str_capital(my_str):
	'''
	Функция делает заглавными первые буквы каждого слова входной строки
	:param my_str: базовая строка
	:return: преобразованная строка
	'''
	return my_str.title()
